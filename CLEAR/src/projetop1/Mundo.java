    public ColorStrategy[][] createColors(int[][] mapa)
    {
        int i, j;
        Str str = new Str();
        ColorStrategy[][] color = new ColorStrategy[30][60];

        for(i = 0; i < 30; i++)
        {
            for(j = 0; j < 60; j++)
            {
                color[i][j] = new BrancoSTR();
                color[i][j] = str.qCor(Integer.toString(mapa[i][j]));
            }
        }
        return color;
    }

    public int[] GeraCoordenadasValidas(int mapa[][])
    {
        Random random = new Random();
        
        int x, y;
         
        y = random.nextInt(60);
        x = random.nextInt(30);
        
        while(mapa[x][y] != 0){
            y = random.nextInt(60);
            x = random.nextInt(30); 
        }
        int vet[] = {x,y};
        
        return vet;
    
    }

